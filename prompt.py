# Reverse mapping for validation
REVERSE_MAPPING = {v: k for k, v in CATEGORY_MAPPING.items()}

# Normalization rules for model outputs (handle variations)
NORMALIZATION_RULES = {
    "شكاوى": "شكوى",
    "الشكاوى": "شكوى",
    "شكوا": "شكوى",
    "الاستفسارات": "استفسار",
      "الاستفسار": "استفسار",
      "استفسارات": "استفسار",
    "اقتراحات": "اقتراح",
    "الإلكترونية": "إلكترونية",
}


def normalize_category(category: str) -> str:
    """
    Normalize category output from model
    Handles plural forms, variations, and typos
    
    Args:
        category: Raw category from model
    
    Returns:
        Normalized category
    """
    # Remove leading/trailing spaces
    category = category.strip()
    
    # Check if already in valid categories
    if category in REVERSE_MAPPING:
        return category
    
    # Check normalization rules
    if category in NORMALIZATION_RULES:
        return NORMALIZATION_RULES[category]
    
    # Fuzzy matching for typos
    valid_categories = list(REVERSE_MAPPING.keys())
    for valid_cat in valid_categories:
        if category.lower() == valid_cat.lower():
            return REVERSE_MAPPING[valid_cat]
        # Check if it's a substring
        if valid_cat in category or category in valid_cat:
            return REVERSE_MAPPING[valid_cat]
    
    # Default: return as is (will fail validation)
    return category

def map_long_to_short(long_name: str) -> str:
    """Map long category name to short form"""
    return CATEGORY_MAPPING.get(long_name, long_name)

def map_short_to_long(short_name: str) -> str:
    """Map short category name to long form"""
    return REVERSE_MAPPING.get(short_name, short_name)

def is_valid_category(category: str) -> bool:
    """Check if category is valid"""
    normalized = normalize_category(category)
    return normalized in REVERSE_MAPPING

def get_all_valid_categories() -> list:
    """Get list of all valid categories"""
    return list(REVERSE_MAPPING.keys())

