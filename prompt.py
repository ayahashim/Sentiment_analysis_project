def validate_and_map_category(model_output: str, strict: bool = False) -> tuple:
    """
    Validate and map category with error handling
    
    Args:
        model_output: Raw output from model
        strict: If True, raise error on invalid category
    
    Returns:
        (normalized_category, is_valid, error_message)
    """
    try:
        # Normalize
        normalized = normalize_category(model_output)
        
        # Validate
        is_valid = is_valid_category(normalized)

        if is_valid:
            normalized = REVERSE_MAPPING[normalized]
        
        elif not is_valid and strict:
            return None, False, f"Invalid category: {model_output}"
        
        return normalized, is_valid, None
        
    except Exception as e:
        return None, False, str(e)





